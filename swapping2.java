package mypack;

class swapping2 {
	public static void main(String args[] ) {

	int x=120, y=100;
	System.out.println("---Before Swapping---");
	System.out.println("first number= "+x);
	System.out.println("second number= "+y);
	
	int temporary=x;
	x=y;
	y=temporary;
System.out.println("---after swap");
System.out.println("first number ="+x);
System.out.println("second number="+y);

	
	
}	
}
